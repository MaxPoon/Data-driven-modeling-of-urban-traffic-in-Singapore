function number=getnumber(x,rgb)
    number='0';
    
    r = rgb(:,:,1);
    g = rgb(:,:,2);
    b = rgb(:,:,3);
    if (r(7,x+5)<15 && g(7,x+5)<20 && b(7,x+5)<20 && r(7,x+5)>5 && g(7,x+5)>10 && b(7,x+5)>10)
        number='2'
    end
    if (r(6,x+4)==0 && g(6,x+4)==0 && b(6,x+4)==0 && r(12,x+4)==0 && g(12,x+4)==0 && b(12,x+4)==0)
        number='1'
    end
    if (r(11,x+1)<15 && g(11,x+1)<15 && b(11,x+1)<15 && r(6,x+6)<240 && g(6,x+6)<240 && b(6,x+6)<240)
        number='9'
    end
    if (r(6,x+4)==0 && g(6,x+4)==0 && b(6,x+4)==0 && r(9,x+2)==255 && g(9,x+2)==255 && b(9,x+2)==255 && rgb(10,x)~=255)
        number='4'
    end
    if (r(7,x+5)>225 && g(7,x+5)>225 && b(7,x+5)>225 && r(7,x+1)==0 && g(7,x+1)==0 && b(7,x+1)==0)
        number='5'
    end
    if (r(6,x+1)==0 && g(6,x+1)==0 && b(6,x+1)==0 && r(10,x+6)<10 && g(10,x+6)<10 && b(10,x+6)<10)
        number='8'
    end
    
    if (r(11,x+1)==0 && g(11,x+1)==0 && b(11,x+1)==0 && r(6,x+5)<15 && g(6,x+5)<15 && b(6,x+5)<15)
        number='3'
    end
    if (r(10,x+1)<5 && g(10,x+1)<5 && b(10,x+1)<5 && r(10,x+6)<15 && g(10,x+6)<15 && b(10,x+6)<15 && r(5,x+1)==255 && g(5,x+1)==255 && b(5,x+1)==255)
        number='6'
    end
    if (r(5,x+1)==0 && g(5,x+1)==0 && b(5,x+1)==0 && r(5,x+6)<28 && g(5,x+6)<28 && b(5,x+6)<28)
        number='7'
    end